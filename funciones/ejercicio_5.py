# Ejercicio: Gestión de una lista de tareas
# Crear un programa que permita gestionar una lista de tareas.
# El programa deberá permitir:
# - Añadir una tarea
# - Listar todas las tareas
# - Marcar una tarea como completada
# - Eliminar una tarea
# - Salir del programa
# Cada tarea deberá tener los siguientes campos:
# - Descripción
# - Completada (un booleano que indica si la tarea está completada o no)